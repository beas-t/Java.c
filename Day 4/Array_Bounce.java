public class Array_Bounce {
    public static void main(String[] args) {
        try {
            int a[] = {1, 2, 3, 4};
            for (int i = 0; i <=5; i++) {
                System.out.println(a[i]);
            }
        } catch (Exception e) {
            System.out.println("Error" + e);
        }


    }
}
